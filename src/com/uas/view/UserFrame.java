     private void autonumber(){
        try{
           java.sql.Connection conn = new DBConnection().connect();
            Statement s = conn.createStatement();
            String sql = "SELECT * FROM user ORDER BY id_user DESC";
            ResultSet r = s.executeQuery(sql);
            if (r.next()) {
                String NoID = r.getString("id_user").substring(2);
                String ID = "" +(Integer.parseInt(NoID)+1);
                String Zero = "";
                
                if (ID.length()==1) 
                {Zero = "00";}
                else if(ID.length()==2)
                {Zero = "0";}
                else if(ID.length()==3)
                {Zero = "";}
                
                txtid_user.setText("US" + Zero + ID);
            }else{
                txtid_user.setText("US001");
            }
            r.close();
            s.close();
        }catch(Exception e){
            System.out.println("autonumber error");
        }
    } 
   
