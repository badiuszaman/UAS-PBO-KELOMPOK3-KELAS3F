private void tampil() {

        us = uc.getAll();
        Object data[][] = new Object[us.size()][9];
        int x = 0;
        for (User u : us) {
            data[x][0] = u.getKode_transaksi();
            data[x][1] = u.getNama_pembeli();
            data[x][2] = u.getNo_hp();
            data[x][3] = u.getAlamat();
             data[x][4] = u.getKode_motor();
              data[x][5] = u.getMerk();
               data[x][6] = u.getWarna();
                data[x][7] = u.getHarga();
                 data[x][8] = u.getTanggal();
                              
            ++x;
        }
        String[] judul = {"KODE TRANSAKSI","NAMA PEMBELI", "NO HP", "ALAMAT", "KODE MOTOR", "MERK", "WARNA", "HARGA", "TANGGAL"};
        table.setModel(new DefaultTableModel(data, judul));
    }
